//SWITCH
const dia = "viernes";

switch(dia){
    case "viernes":
        console.log("Hoy es viernes");
        break;
    case "sabado":
        console.log("es sabado");
        break;
    case "domingo": 
        console.log("Es domingo");
        break;
    case "lunes":
        console.log("Es lunes");
        break;
    case "martes": 
        console.log("es martes");
        break;
    case "miercoles":
        console.log("es miercoles");
        break;
    case "jueves": 
        console.log("es jueves");
        break;
    default: 
        console.log("no es ningun dia valido");
        break;
}